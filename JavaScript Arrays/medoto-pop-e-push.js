// O push e pop são usados para adicionar/remover elementos de uma lista, como mostra o seguinte código:
jantarDeHoje = ['🍔', '🌭', '🍕']

jantarDeHoje.pop()
jantarDeHoje.pop()

jantarDeHoje.push('🍳')
jantarDeHoje.push('🥗')
jantarDeHoje.push('🍏')

console.log(jantarDeHoje)

// retornan "[ '🍔', '🍳', '🥗', '🍏' ]"